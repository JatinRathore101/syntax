// Hello World - The starting point of every Java program

public class Main {
    // Every Java program needs a main method - it's the entry point
    public static void main(String[] args) {
        // System.out.println prints text to the console followed by a newline
        System.out.println("Hello, World!");

        // System.out.print prints without a newline
        System.out.print("Hello ");
        System.out.print("Java!\n");

        // printf allows formatted output (like in C)
        System.out.printf("Java version: %d%n", 21);
    }
}
